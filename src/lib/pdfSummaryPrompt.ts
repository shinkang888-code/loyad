import { analyzeJudgmentStructure } from "@/lib/pdfTextExtract";

export function buildJudgmentSummaryPrompt(text: string): string {
  const structureHints = analyzeJudgmentStructure(text);
  const structureLine =
    structureHints.length > 0
      ? `【감지된 판결문 구조】 ${structureHints.join(", ")}\n\n`
      : "";

  return `당신은 형사·민사 판결문을 구조화하여 요약하는 전문가입니다. 아래 판결문을 읽고, **형사 판결문**이면 다음 규칙을 정확히 따르세요.

${structureLine}【형사 판결문일 때의 채우기 규칙】
- **1) 사건의 개요**: 본문에서 '범죄사실' 또는 '범 죄 사 실' 제목 **아래**에 나오는 내용을 요약하여 넣으세요. 해당 제목이 없으면 사건의 핵심 사실을 요약하세요.
- **2) 주요 쟁점**: 본문에서 '1. 주장의 요지' 또는 '주장의 요지' **아래**에 나오는 피고인·변호인 주장 내용을 그대로 삽입하세요. 없으면 쟁점을 요약하세요.
- **3) 법원의 판단 (인용/기각 사유)**: 본문에서 '피고인 및 변호인의 주장에 대한 판단' 또는 '당심의 판단' 등 절 안에 있는 **'판단'** 소제목 **이하** 본문을 요약하거나 핵심만 넣으세요. 인용/기각 이유를 포함하세요.
- **4) 결론 (주문)**: 본문에서 **'주 문'** 또는 **'주문'** 제목 **아래** 한 문단을 그대로 넣으세요. 반드시 주문 문구를 포함하세요.
- **5) 실무적 시사점**: 위 내용을 바탕으로 2~3문장으로 실무적 시사점을 작성하세요.

【출력 형식】 반드시 아래 5개 제목을 그대로 쓰고, 그 다음 줄부터 해당 내용을 작성하세요.
1) 사건의 개요
2) 주요 쟁점
3) 법원의 판단 (인용/기각 사유)
4) 결론 (주문)
5) 실무적 시사점

민사·행정 등 형사가 아닌 판결문이면, 위 5개 항목에 맞게 본문 구조를 참고하여 채우세요.

판결문:
${text.slice(0, 30000)}`;
}
